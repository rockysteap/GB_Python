# file-modes:

# a - (append) открытие для записи (добавления -> курсор в конце)
#   using:
#   -> writes to file appending from the end
#   -> creates new file if no file found

# r - (read) открытие для чтения данных
#   using:
#   -> reads from file
#   -> returns error if no file found

# w - (write) открытие для записи (курсор в начале)
#   using:
#   -> writes to file from beginning
#   -> creates new file if no file found

# w+ - (write+) открытие для записи (курсор в начале) и чтения
#   using:
#   -> writes to file from beginning
#   -> reads from file
#   -> creates new file if no file found

# r+ - (read+) открытие для чтения данных и записи (добавления -> курсор в конце)
#   using:
#   -> reads from file
#   -> writes to file appending from the end
#   -> returns error if no file found





